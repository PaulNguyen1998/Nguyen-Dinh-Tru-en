package Lab5;

public class SanPham 
{
	private String tenSP;
	private int giaSP;
	
	public SanPham(String tenSP, int giaSP) 
	{
		this.tenSP = tenSP;
		this.giaSP = giaSP;
	}
	
	public String getTenSP() 
	{
		return this.tenSP;
	}
	
	public int getGiaSP() 
	{
		return this.giaSP;
	}
	
}
